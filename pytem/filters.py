# ============================================================================
# Euler acceleration weights (Abate & Whitt, 1995)
# ============================================================================

_EULER_A = 18.4  # Bromwich abscissa parameter (~8 digits accuracy)

def _euler_weights(N):
    eta = np.ones(2 * N + 1)
    eta[0] = 0.5
    binom_cum = 0.0
    binom = 1.0
    for j in range(N):
        binom_cum += binom / (2.0**N)
        eta[N + 1 + j] = 1.0 - binom_cum
        binom *= float(N - j) / float(j + 1)
    return eta

